    private void jPanel2MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel2MousePressed
        // TODO add your handling code here:
        int c;
        String IdUsuario,IdLibro,FechaEntrada,FechaSalida;
        long Dias;
        LogPrestamo aux;
        try {
            c = jTable1.getSelectedRow();
            aux = Main.Storage.Prestamos.get(c);
            FechaEntrada = JOptionPane.showInputDialog("Fecha de Entrada:");
            aux.setFechaEntrada(LocalDate.parse(FechaEntrada,DateTimeFormatter.ISO_LOCAL_DATE));
            aux = Main.Storage.Prestamos.get(c);
            FechaSalida = JOptionPane.showInputDialog("Fecha de Salida:");
            aux.setFechaSalida(LocalDate.parse(FechaSalida,DateTimeFormatter.ISO_LOCAL_DATE));
            Duration diff = Duration.between(aux.getFechaEntrada().atStartOfDay(), aux.getFechaSalida().atStartOfDay());
            Dias = diff.toDays();
            aux = Main.Storage.Prestamos.get(c);
            aux.setDias(Dias);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Escoje una fila");
        }
        cargarDatos();
    }//GEN-LAST:event_jPanel2MousePressed

    private void jPanel3MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel3MousePressed
        // TODO add your handling code here:
        int c;
        try {
            c= jTable1.getSelectedRow();
            Main.Storage.Prestamos.remove(c);    
            Libro Aux = Main.Storage.Libros.get(c);
            Aux.setStatus(true);
            Main.Storage.Libros.set(c, Aux);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Escoje una fila para eliminar");
        }
        cargarDatos();
    }//GEN-LAST:event_jPanel3MousePressed


