    private void cargarDatos() {
        String Mat[] [] = new String[Main.Storage.Prestamos.size()][6];
        LogPrestamo aux;
        for (int i = 0; i < Main.Storage.Prestamos.size(); i++) {
            aux = Main.Storage.Prestamos.get(i);
            
            Mat [i][0]= Integer.toString(aux.getIdPrestamo());
            Mat [i][1]= Integer.toString(aux.getIdUsuario());
            Mat [i][2]= Integer.toString(aux.getIdLibro());
            Mat [i][3]= aux.getFechaSalida().toString();
            Mat [i][4]= aux.getFechaEntrada().toString();;
            Mat [i][5]= Float.toString(aux.getDias());
        }
        jTable1.setModel(new javax.swing.table.DefaultTableModel(Mat,new String [] {"ID Prestamo", "ID Usuario", "ID Libro", "Fecha Salida", "Fecha Entrada", "Días"}));
    }

        
        
        
        
        
